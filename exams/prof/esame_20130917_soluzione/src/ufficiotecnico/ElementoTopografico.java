package ufficiotecnico;

public class ElementoTopografico {

	private String nome;
	private int codice;
	
	public String getNome() {
		
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome=nome;
	}
	
	public int getCodice() {
		
		return codice;
	}
	
	public void setCodice(int codice) {
		this.codice=codice;
	}
}
